'''
Module to access the OpenAI api
'''

# =============================================================================================
# =========IMPORTS=============================================================================
import os
import openai
openai.api_key = os.getenv("OPENAI_API_KEY")


# =============================================================================================
# =========CONSTANTS===========================================================================
GPT_VERSION = 'gpt-3.5-turbo'

# =============================================================================================
# =========FUNCTIONS===========================================================================


def call_gpt(input):
    chat_call = openai.ChatCompletion.create(
        model=GPT_VERSION,
        messages=[{
                "role": "user",
                "content": input},
        ],
    )
    return chat_call.choices[0].message
